			$this->data['type'] = $this->request->post['type'];
		} elseif (!empty($card_info)) {
			$this->data['type'] = $card_info['type'];
		} else {
			$this->data['type'] = '';
		}


		if (isset($this->request->post['images'])) {
			$this->data['images'] = $this->request->post['images'];
