			$card_images = $this->request->post['images'];
		} elseif (isset($this->request->get['id'])) {
			$card_images = $this->model_catalog_card->getCardImages($this->request->get['id']);
		} else {
			$card_images = array();
		}

		$this->data['card_images'] = array();

